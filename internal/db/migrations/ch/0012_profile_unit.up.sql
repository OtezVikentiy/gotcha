-- Единица измерения веса выборки из pprof (SampleType.Unit): 'nanoseconds',
-- 'bytes', 'count' и т.п. Раньше сохранялось только имя типа профиля ('cpu'),
-- а единица отбрасывалась, и на показ она выводилась по таблице соответствий
-- «тип → единица», зашитой в UI. Для нестандартных типов профилей такая
-- догадка не работала.
--
-- DEFAULT '' — у строк, записанных до этой миграции, единицы нет; UI в этом
-- случае возвращается к прежней догадке по типу профиля.
ALTER TABLE profile_samples ADD COLUMN unit LowCardinality(String) DEFAULT '';
