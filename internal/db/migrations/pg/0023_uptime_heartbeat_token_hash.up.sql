-- Heartbeat-токены мониторов теперь хранятся как sha256(token) в bytea — так
-- же, как probe-токены (probes.token_hash) и session-токены. Сырой
-- heartbeat_token из таблицы удаляется, чтобы токен не лежал в БД в открытом
-- виде.
--
-- Бэкфилл существующих токенов НЕВОЗМОЖЕН: посчитать sha256 внутри SQL можно
-- только через pgcrypto (digest()), а эта инсталляция pgcrypto не использует
-- (из расширений подключён лишь citext, см. 0001_init). Включать pgcrypto
-- ради одной миграции мы не станем, а пересчитать хеш в Go на этапе миграции
-- нельзя — миграции здесь чисто SQL-овые. Поэтому новая колонка остаётся
-- пустой (NULL) для всех уже существующих строк.
--
-- ПОСЛЕДСТВИЕ: у ранее созданных heartbeat-мониторов heartbeat_token_hash
-- будет NULL; их прежние heartbeat-URL перестанут находиться (ByHeartbeatToken
-- ищет по хешу входящего токена) и перестанут работать. Такие мониторы нужно
-- пересоздать — при создании генерируется новый токен, и он сразу сохраняется
-- уже в виде хеша. Мониторы, созданные после этой миграции, работают штатно:
-- входящий из URL сырой токен хешируется и ищется по heartbeat_token_hash.
ALTER TABLE monitors ADD COLUMN heartbeat_token_hash bytea UNIQUE;
ALTER TABLE monitors DROP COLUMN heartbeat_token;
